		public Map() {
			list = new ArrayList<Element>();
			for (int i = 0; i < array.length; i++) {
				for (int j = 0; j < array[i].length; j++) {
					//读取到的是1，画砖头
					if(array[i][j]==1){
						// x
						brick = new Brick(j*45,i*45,45,45,new ImageIcon("image/brick.png").getImage());
						list.add(brick);
					}
					//读到2画金币
					if(array[i][j]==2){
						basketball = new Basketball(j*45,i*45,45,45,new ImageIcon("image/basketball.png").getImage());
						list.add(basketball);
					}
					if(array[i][j]==3){
						pinkzhuan = new PinkZhuan(j*45,i*45,45,45,new ImageIcon("image/fenzhuan.png").getImage());
						list.add(pinkzhuan);			
					}
					if(array[i][j]==4){
						qiang = new Qiaobiluo(j*45,i*45,120,140,new ImageIcon("image/timg.png").getImage());
						list.add(qiang);				
					}
					if(array[i][j]==5){
						jianci = new Jianci(j*45,i*45,45,45,new ImageIcon("image/jianci1.gif").getImage());
						list.add(jianci);
					}
				}	
		}
	}
		public ArrayList<Element> getList() {
			return list;
		}
		public void setList(ArrayList<Element> list) {
			this.list = list;
		}
